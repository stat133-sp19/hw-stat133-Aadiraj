iguodala <- mutate(iguodala, minute = (period - 1)*12 + 12 - minutes_remaining)
green <- mutate(green, minute = (period - 1)*12 + 12 - minutes_remaining)
durant <- mutate(durant, minute = (period - 1)*12 + 12 - minutes_remaining)
